#常量
#用全部大写的变量名表示常量

#整数除法有两种
# / 普通除法
print('10/ = ',10/3)
print('9/3 = ',9/3)
# // 地板除
print('10//3 = ',10//3)
print('9//3 = ',9//3)
# % 取余数除
print('100%3 = ',10%3)
print('90%3 = ',90%3)
print(7%3)